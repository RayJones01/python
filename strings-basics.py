# Challenge: Strings Basics

'''
Instructions-
1. Create a variable txt with the value "Hello, World!"
2. Print the characters from index 2 to 5 (slicing)
3. Print txt converted to upper case
4. Create a variable name with the value "Python"
5. Use an f-string to print "I love Python" using the name variable
'''

# Create the variable
txt = "Hello, World!"

# Print characters from index 2 to 5
print(txt[2:5])

# Print in upper case
print(txt.upper())

# Create the name variable
name = "Python"

# Print using an f-string
print(f"I love {name}")
